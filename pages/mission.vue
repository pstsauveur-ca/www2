          <h3 class="display-4 mb-5">Notre mission</h3>
          
          <p>« Être et faire des disciples. » Cette mission nous vient du Christ. Elle
          est celle qui résume le projet qu’il a pour chacun de nous. Devenir
          disciple, décrit que le désir du Père est l’union entre lui et nous. Se
          mettre à sa suite, mettre nos pas dans les siens, de manière à nous
          permettre de devenir pleinement ce que nous sommes, ses enfants.</p>
          
          <p>Le deuxième mouvement, tout aussi important, qui découle de la
          première relation est celui de la mission. Le disciple est
          nécessairement missionnaire, c’est-à-dire, qu’il développe, de son
          union personnelle avec Dieu, le désir d’aller vers les autres frères et
          sœurs pour les aimer en son Nom. Le vrai amour envers le frère et la
          sœur, est de leur permettre de réaliser et d’expérimenter à leur tour,
          combien Dieu les aime.</p>

          <h3 class="display-4 mb-5">Notre vision</h3>

          <p>Pour les paroisses qui nous ont été
          confiées, nous rêvons d’une
          communauté stimulée et stimulante.
          Une communauté qui vit en
          profondeur sa foi reçue de Dieu,
          annoncée par Jésus. Une communauté
          sincère et cohérente avec l’appel du
          Christ à vivre une vie nouvelle.
          </p>

          <p>De la mission confiée par le Christ,
          découle les attitudes et l’ambiance que
          vit notre communauté. Nous sommes
          donc des personnes qui, parce que
          nous avons été profondément
          touchées au cœur par Dieu, accueillent
          les autres à Sa manière. Cette
          communauté considère son prochain
          comme une personne à aimer et non
          comme un obstacle à sa liberté.
          </p>

          <p>Chaque personne, qu’elle soit
          de passage, pratiquante
          régulière ou qui souhaite
          redécouvrir sa foi chrétienne,
          est accueillie et considérée.
          Plusieurs choses lui sont
          offertes, de manière à ce
          qu’elle répondre aux besoins
          qu’elle porte. Nous prenons
          ses coordonnées pour garder
          contact avec elle. De cette
          façon, elle découvre sa place
          dans la communauté. Notre
          accueil, toujours vécu dans la
          lumière du Christ qui est la
          Vérité, enlève la solitude,
          l’exclusion, la condamnation
          et remet la personne sur un
          chemin de vérité et de liberté.</p>

          <p>Ici, les gens répondent aux questions qu’ils
          portent parce qu’ils ont trouvé le sens
          profond à la vie que Dieu nous révèle. Ils
          sont capables d’en témoigner et souhaitent
          proposer à d’autres de trouver ou de
          retrouver le sens de leur vie. On se sent
          épaulé dans nos moments de
          découragements ou d’épreuves. On réussit
          à apprécier la vie insufflée par Dieu qui
          nous permet de devenir toujours plus libre,
          car il nous sauve. Parce que la société
          n’arrive pas à donner les bases solides
          d’une vie humaine pleinement vécue, la
          paroisse offre aux gens de s’enraciner dans
          ce qu’ils sont profondément, tout en leur
          permettant de toujours se laisser interpeller
          à faire de nouveaux pas libérateurs. Étant
          donné que la communauté est préoccupée
          de la détresse toujours plus grande, de
          l’anxiété et des blessures chez les gens, elle
          bâtie un réseau d’aide permettant de ne
          laisser personne dans la souffrance et le
          mal, car les solutions sont à notre portées.
          </p>

          <p>Les gens, avant l’eucharistie, savent se saluer et prennent le temps, ensemble,
          de s’accueillir. On peut voir la joie sur les visages de se retrouver. Cette
          fraternité vécue avec sincérité mène à la célébration des saints Mystères du
          Seigneur. Ensemble, on rend grâce des merveilles que Dieu fait dans chacune
          de nos vies et on demande au Seigneur de nous soutenir dans nos faiblesses
          afin de recevoir les grâces nécessaires pour la semaine qui débute.
          </p>

          <p>Les formations en vue de recevoir un sacrement, sont concentrées le
          dimanche, autour de la célébration de la résurrection de Notre Seigneur.
          Différentes personnes de la communauté, parce qu’elles sont formées et
          possèdent un charisme dans le domaine, accompagnent et forment les
          personnes pour qu’elles saisissent davantage la beauté d’être à leur tour un
          disciple-missionnaire. Les gens ne demandent plus de recevoir un sacrement
          mais demandent plutôt à l’équipe en place, l’aide nécessaire pour devenir un
          meilleur disciple du Christ. Comme le sacrement n’est plus perçu comme un
          objectif en soit, il vient plutôt ponctuer la marche du disciple qui reçoit les
          grâces du moment afin de lui permettre de poursuivre sa route vers la
          sainteté.
          </p>

          <p>Graduellement, les personnes qui progressent dans leur foi se voient confier
          un ministère, leur permettant de mettre à contribution les talents et les forces
          que le Seigneur a déposées en eux. Les chrétiens se sentent partie prenante de
          leur communauté et la servent spontanément avec un cœur généreux.
          Personne n’est indifférent au besoin de sa communauté, car il réalise qu’il est
          membre d’une famille.</p>

          <p>La communauté prie et réfléchie régulièrement afin de toujours mieux lire les
          signes des temps et, enracinée dans notre monde, elle cherche à répondre de
          manière chrétienne aux différents défis de notre génération. Avec
          imagination, elle dépense ses énergies au service de ses frères et sœurs.
          Aucune initiative pouvant rendre service au prochain et signifiant notre
          amour pour le Christ, n’est mise de côté.</p>

          <p>Des leaders émergent au fil des années, soutenus par des expériences de
          prières, des rencontres de formations et le côtoiement des membres de la
          communauté. Plus ils deviennent forts et adultes dans leur foi, plus la
          communauté a les yeux fixés sur le Christ et vit de son seul amour. Les
          différentes vocations, que ce soit le mariage, la vie religieuse, le sacerdoce,
          sont misent en valeur. On encourage chacun à découvrir la beauté de
          l’engagement vocationnel et à le déployer dans la fidélité, au service de
          l’Église. On amène les différentes vocations à l’excellence.</p>

          <p>Notre communauté prend conscience que si belle soit elle, elle ne peut
          s’isoler ou se replier sur elle-même. Elle désire toujours demeurer en
          ouverture. Le disciple-missionnaire est celui qui s’engage à redonner
          généreusement ce qu’il a reçu. De ce fait, d’autres paroisses bénéficient de
          son rayonnement de plusieurs façons. Notre communauté forme et soutient
          d’autres communautés paroissiales, afin qu’elle puisse vivre le sens premier
          pour lequel elle existe, c’est-à-dire « être et faire des disciples ». Le
          mouvement bien enclenché dans notre communauté du disciplemissionnaire appelé à l’excellence, provoque ce désir de vivre en Église et
          non en compétition.</p>

          <p>Parce que nous vivons intensément tout ce qui vient d’être énuméré
          précédemment, les gens voient que cette communauté vit la parole de Jésus :
          « À ceci, tous reconnaîtront que vous êtes mes disciples : si vous avez de
          l’amour les uns pour les autres ». (Jn 13,35)</p>

          <p>C’est ce que vous vivrez si vous vous engagez avec nous!</p>


